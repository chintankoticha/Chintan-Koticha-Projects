    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) messageBoardJTable.getModel();

        model.setRowCount(0);
        for (WorkRequest request : userAccount.getCustomer().getWorkQueue().getWorkRequestList()) {
            Object[] row = new Object[5];
            row[0] = request;
            row[1] = request.getRequestDate();

            model.addRow(row);
        }
    }

